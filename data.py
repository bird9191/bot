def get_causes(answers: list) -> list:
    causes = [
        label
        for label, indices in CAUSES_MAP
        if any(qi < len(answers) and answers[qi]["score"] >= 2 for qi in indices)
    ]
    return causes[:5] or ["профилактический контроль витаминов и минералов"]
